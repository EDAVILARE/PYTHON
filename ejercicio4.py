import math #importa el valor de pi
radio = float(input("Ingrese el radio: "));
#pi = 3.1416;
area = math.pi * radio * radio; # o pi * radio * radio;
longitud = 2 * math.pi * radio; # o 2 * pi * radio;
print(f"El area es: {area:.3f} y la longitud es: {longitud:.3f}"); #arroja el resultado con 3 decimales















