print (lista[0]);#imprime la primera posicion
print(lista[1]);#impirme la segunda posicion
print (lista[-1]);#imprime la ultima posicion
print (lista[-2]);#imprime la penultima posicion
print (lista[0:2]);#imprime desde la posicion 0 hasta la 1 [0,1].
print (lista[0:4]);#imprime desde la posicion 0 hasta la 3 [0,1,2,3].
print(lista[:]) #imprime toda la lista
print(lista[1:]); #imprime desde la posicion 1 hasta la ultima
print(lista[:3]); #imprime desde la posicion 0 hasta la 2 [0,1,2].
print(lista[4]) #imprime la posicion 4 [1,2,3,4,5]























